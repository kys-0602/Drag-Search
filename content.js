// 팝업 생성 함수
function createSearchPopup(selectedText, selectionRect) {
  removeSearchPopup();

  // 새 팝업 요소 생성
  searchPopup = document.createElement('div');
  searchPopup.className = 'drag-search-popup';

  // 스크롤 컨테이너
  const scrollContainer = document.createElement('div');
  scrollContainer.className = 'search-engines-container';

  // 검색 엔진 아이콘 추가
  searchEngines.forEach(engine => {
    const link = document.createElement('a');
    link.className = 'search-engine-link';
    link.href = engine.url + encodeURIComponent(selectedText);
    link.target = '_blank';
    link.title = engine.name;
    link.innerHTML = engine.icon;
    
    // 클릭 시 팝업 제거
    link.addEventListener('click', () => {
      removeSearchPopup();
    });

    scrollContainer.appendChild(link);
  });

  searchPopup.appendChild(scrollContainer);
  document.body.appendChild(searchPopup);

  // 팝업 크기 측정
  const popupRect = searchPopup.getBoundingClientRect();
  const viewportWidth = window.innerWidth;
  const viewportHeight = window.innerHeight;
  const scrollX = window.pageXOffset || document.documentElement.scrollLeft;
  const scrollY = window.pageYOffset || document.documentElement.scrollTop;
  
  // 선택 영역의 중앙 좌표 계산
  const selectionCenterX = selectionRect.left + selectionRect.width / 2;
  
  // 간격
  const gap = 16;
  
  // 가로 여유 공간이 충분한지 확인
  const hasEnoughWidth = viewportWidth > 400; // 최소 400px 이상이면 가로
  
  // 가로/세로 레이아웃 결정
  if (hasEnoughWidth) {
    searchPopup.classList.add('horizontal');
  } else {
    searchPopup.classList.add('vertical');
  }
  
  // 레이아웃 변경 후 다시 측정
  const finalPopupRect = searchPopup.getBoundingClientRect();
  
  // 기본 위치: 선택 영역 하단 중앙
  let popupX = selectionCenterX - finalPopupRect.width / 2;
  let popupY = selectionRect.bottom + gap;
  
  // 하단에 공간이 부족하면 상단에 표시
  if (selectionRect.bottom + finalPopupRect.height + gap > viewportHeight) {
    popupY = selectionRect.top - finalPopupRect.height - gap;
  }
  
  // 좌우 경계 체크
  if (popupX < gap) {
    popupX = gap;
  } else if (popupX + finalPopupRect.width > viewportWidth - gap) {
    popupX = viewportWidth - finalPopupRect.width - gap;
  }
  
  // 상단 경계 체크 (상단에도 공간이 부족한 경우 최대한 보이도록)
  if (popupY < gap) {
    popupY = gap;
  }
  
  // 스크롤 위치 반영하여 절대 위치 설정
  searchPopup.style.left = `${popupX + scrollX}px`;
  searchPopup.style.top = `${popupY + scrollY}px`;
}

// 팝업 제거 함수
function removeSearchPopup() {
  if (searchPopup) {
    searchPopup.remove();
    searchPopup = null;
  }
}

// 텍스트 선택 완료 이벤트 리스너
document.addEventListener('mouseup', (event) => {
  setTimeout(() => {
    const selection = window.getSelection();
    const selectedText = selection.toString().trim();
    
    // 선택된 텍스트가 있는 경우에만 팝업 표시
    if (selectedText.length > 0 && selection.rangeCount > 0) {
      const range = selection.getRangeAt(0);
      const rect = range.getBoundingClientRect();
      createSearchPopup(selectedText, rect);
    } else {
      removeSearchPopup();
    }
  }, 10);
});

// 팝업 외부 클릭 시 팝업 제거
document.addEventListener('mousedown', (event) => {
  if (searchPopup && !searchPopup.contains(event.target)) {
    removeSearchPopup();
  }
});

// 텍스트 선택 해제 시 팝업 제거
document.addEventListener('selectionchange', () => {
  const selectedText = window.getSelection().toString().trim();
  if (selectedText.length === 0 && searchPopup) {
    setTimeout(() => {
      const stillNoSelection = window.getSelection().toString().trim();
      if (stillNoSelection.length === 0) {
        removeSearchPopup();
      }
    }, 100);
  }
});
